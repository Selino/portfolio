<h1>BUZZWORDS MAKE THE WEB GO AROUND</h1>
<h3>Sometimes folks just want the quick and dirty on what I know about a few key words. Think of this as the Cliffs Notes of Selino Valdes.</h3>
<div class="word">Agile Development</div>
<div class="def">Self-organization and accountability has been the crux of my development approach from day one. I was "agile" and I didn't even know it!</div>

<div class="word">Akamai</div>
<div class="def">Three consecutive years of distributing hundreds of files via the world's largest server distribution network.</div>

<div class="word">Ajax</div>
<div class="def">Yes, I know how to use Ajax methods. The gallery scroller uses Ajax methods to call the images and text you see displayed on the main page. I am also familiar with calls being made from a field on a page (ie: dynamic checking of valid user names in sign up forms). JQuery has been instrumental in allowing me to make various Ajax functions and not worry about browser compatibility issues.</div>

<div class="word">Hungarian Notation</div>
<div class="def">I'll confess that I have made some pretty messy code in my life but it's always come back to haunt me. I've found that having a pseudo-standard method of writing has really been helping me keep things tidy.</div>

<div class="word">LAMP</div>
<div class="def">Linux, Apache, MySQL, and PHP (not so much Python or Perl for me) has been at the core of my freelance development over the past 5+ years.</div>

<div class="word">Progressive Enhancement</div>
<div class="def">Making sure that a browser can see your information without choking that client with features that it cannot display is a must on today's web.</div>

<div class="word">REST</div>
<div class="def">I have to admit that I kinda cheated when it came to building my first REST app for www.flojuggler.com. I used CakePHP and it made the whole process so easy I actually feel guilty.</div>

<div class="word">SEO</div>
<div class="def">Search Engine Optimization has become the holy grail of the marketing department. If you examine the source code for www.selino.com you'll notice that all the appropriate <em>White Hat</em> strategies are present and working.</div>

<div class="word">Social Networking</div>
<div class="def">Facebook integration is the next big bubble... uh... I mean wave. Integrating Facebook Connect was one of my major tasks on the www.flojuggler.com project.</div>

<div class="word">Source Control</div>
<div class="def">Currently I am a huge fan of Subversion combined with Tortoise SVN but I also have years of experience using CVS via command line and SmartCVS in team environments.</div>

<div class="word">Startup Environment</div>
<div class="def">Yes, I'm uncomfortable being comfortable. &quot;Learn something new every day&quot; is my motto along with &quot;We stay until it's done&quot;.</div>
